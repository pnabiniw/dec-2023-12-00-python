# There are broadly two types of datatypes in Python
# 1. Mutable Datatypes
# 2. Immutable Datatypes


# 1. Mutable Datatypes: All those objects which can be modified after their creation are the
# mutable objects/datatypes. E.g List, Dictionary, Set

# 2. Immutable Datatypes: All those objects which can't be modified after their creation are the
# immutable objects. E.g. String, Tuple, Numbers, Boolean.


